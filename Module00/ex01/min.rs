fn min(a: i32, b: i32) -> i32 {
	if a < b {
		a
	}
	else {
		b
	}
}

fn main() {
	let a = 50;
	let b = 42;
	let c = 600;

	println!("{}", min(a, b));
	println!("{}", min(c, b));
	println!("{}", min(c, a));
}