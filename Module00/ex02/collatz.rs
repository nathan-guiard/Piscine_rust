fn collatz(start: u32) {
	let mut x = start;
	
	println!("{}", x);
	while x != 1 {
		match x % 2 {
			0 => x /= 2,
			1 => x = x * 3 + 1,
			_ => x = x,
		}
		println!("{}", x);
	}
}

fn main() {
	collatz(3);
	println!("---");
	collatz(47);
}