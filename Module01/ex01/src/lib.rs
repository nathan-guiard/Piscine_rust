fn min<'a>(a: &'a i32, b: &'a i32) -> &'a i32 {
	if a < b {
		a
	} else {
		b
	}
}

#[cfg(test)]
mod tests {
	use crate::min;

    #[test]
    fn working() {
        let a = 60;
		let b = 70;
		let c = 700;
	
		assert_eq!(min(&a, &b), &a);
		assert_eq!(min(&c, &b), &b);
    }
}
