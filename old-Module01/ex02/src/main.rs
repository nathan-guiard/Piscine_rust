pub fn min<'a>(a: &'a i32, b: &'a i32) -> &'a i32 {
	if *a < *b {
		a
	}
	else {
		b
	}
}

fn main() {
	let a: i32 = 50;
	let b: i32 = 84;
	let res: & i32 = min(&a, &b);

	println!("{}", *res);

	let res: & i32 = min(&b, &a);
	println!("{}", *res);
}