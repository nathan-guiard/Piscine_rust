fn main() {
	for x in 1..=100 {
		match (x % 3, x % 5, x % 11) {
			(0, 0, _) => println!("fizzbuzz"),
			(0, _, _) => println!("fizz"),
			(_, 0, _) => println!("buzz"),
			(_, _, 3) => println!("FIZZ"),
			(_, _, 5) => println!("BUZZ"),
			_		  => println!("{}", x)
		}
	}
}